package com.domin;

//月仓库出库
public class MonthOutOfStock {
    private int month;//月份
    private int num;//当月出库数

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
